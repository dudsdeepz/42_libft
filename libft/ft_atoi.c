#include "libft.h"

int	ft_atoi(const char *nptr)
{
	size_t		i;
	long int	result;
	int			sign;

	sign = 1;
	result = 0;
	i = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9' && nptr[i])
	{
		result = result * 10 + nptr[i] - '0';
			i++;
	}
	return (result * sign);
}
