#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;

	a = 0;
	while (*s != '\0')
	{
		if (*s == c)
			a = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (a);
}
